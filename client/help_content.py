def get_help_html(program_version: str) -> str:
    """
    Возвращает html-код для вывода в окно руководства пользователю.
    Args:
        program_version: Версия программы

    Returns:

    """
    return f"""
    <h3>Проекты</h3>
    <ul>
        <li><b>Добавление:</b> нажмите «➕ Добавить» на левой панели, укажите имя и, при необходимости, путь к папке проекта. Если путь указан, автоматически парсятся технологии из <code>requirements.txt</code>.</li>
        <li><b>Удаление:</b> выберите проект и нажмите «🗑️ Удалить» (или клавишу <kbd>Delete</kbd>).</li>
        <li><b>Редактирование:</b> кнопка «✏️ Редактировать» позволяет изменить имя, описание, путь и технологии.</li>
        <li><b>Избранное:</b> двойной клик по проекту ставит/снимает звёздочку — избранные проекты всегда вверху списка.</li>
    </ul>

    <h3>Задачи</h3>
    <ul>
        <li><b>Добавление:</b> кнопка «➕ Добавить» (или <kbd>Ctrl+T</kbd>) открывает диалог, где можно указать заголовок, описание, заметку, код и, при необходимости, родительскую задачу (для создания подзадачи).</li>
        <li><b>Множественное создание:</b> кнопка «📋 Подзадачи» позволяет быстро добавить несколько задач в формате <code>- Заголовок. Описание</code>.</li>
        <li><b>Удаление:</b> выберите задачу и нажмите «❌ Удалить» (или <kbd>Delete</kbd>). Если у задачи есть подзадачи, программа спросит, удалить ли только её или всё поддерево.</li>
        <li><b>Архивация/Восстановление:</b> кнопки «📦 В архив» и «↩️ Восстановить» действуют на всё поддерево задач.</li>
        <li><b>Избранное:</b> двойной клик по задаче ставит/снимает звёздочку — избранные задачи всегда вверху списка.</li>
    </ul>

    <h3>Статусы задач</h3>
    <ul>
        <li><b>Новое</b> — задача только создана.</li>
        <li><b>В процессе</b> — работа идёт.</li>
        <li><b>Готово</b> — задача выполнена.</li>
        <li><b>Архивное</b> — задача в архиве (скрыта, если не включён соответствующий фильтр).</li>
    </ul>
    <p>Статус можно изменить в панели деталей задачи (выпадающий список) или, для подзадач, чекбоксом в дереве.</p>

    <h3>Подзадачи (иерархия)</h3>
    <ul>
        <li>Задачи могут иметь неограниченную вложенность.</li>
        <li>При добавлении задачи можно указать родителя.</li>
        <li>Статус родителя автоматически обновляется: если все подзадачи «Готово» → родитель становится «Готово»; если хотя бы одна не готова → «В процессе».</li>
        <li>Архивация, восстановление и удаление действуют на всё поддерево.</li>
    </ul>

    <h3>Шаблоны задач</h3>
    <ul>
        <li>Меню «Настройки → Управление шаблонами задач» позволяет создавать, редактировать и удалять готовые шаблоны.</li>
        <li>На панели задач есть кнопка «📋 Шаблоны» — откроется окно выбора шаблонов для быстрого добавления в текущий проект.</li>
    </ul>

    <h3>Поиск</h3>
    <p>Поле поиска над списком проектов ищет по названию и технологиям (без учёта регистра).</p>

    <h3>Горячие клавиши</h3>
    <ul>
        <li><kbd>Ctrl+P</kbd> — новый проект</li>
        <li><kbd>Ctrl+T</kbd> — новая задача</li>
        <li><kbd>Ctrl+=</kbd> (или <kbd>Ctrl++</kbd>) — развернуть все задачи</li>
        <li><kbd>Ctrl+-</kbd> — свернуть все задачи</li>
        <li><kbd>Delete</kbd> — удалить выбранный проект или задачу</li>
    </ul>

    <h3>Настройки</h3>
    <ul>
        <li>В меню «Настройки → Общие» можно выбрать тему (светлая/тёмная/системная) и указать путь к PyCharm для открытия проектов.</li>
        <li>Кнопка «🚀 Открыть в PyCharm» появляется только при включённой опции и указанном пути.</li>
    </ul>

    <p style="margin-top: 15px; color: gray;">Версия программы: {program_version}</p>
    """